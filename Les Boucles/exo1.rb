

# Exemple concret, 

# Je crée une variable i qui est égal à 0
# et tant que i est inférieur à 10 
    # j'incrémente i en faisant i plus 1
# end    

# Explication du code, 
# i vaut 0 au départ while demande si la condition i = 0 et inférieur à 10, donc oui i est bien inférieur à 10 
# donc un tour de boucle se passe et incrémente i de +1, donc i qui valait 0, vaut maintenant 1. 
# Mais i reste cependant inférieur à 10 durant ce tour de boucle donc l'opération se repètera tant que i sera inférieur à 10 

i = 0
while i < 10
    i = i + 1
end